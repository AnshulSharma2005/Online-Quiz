function displayQuestion() {
    const questionData = currentQuestions[currentQuestionIndex];
    document.getElementById('question').textContent = questionData.text;

    const answersContainer = document.getElementById('answers');
    answersContainer.innerHTML = ''; // Clear previous answers

    questionData.answers.forEach((answer) => {
        const button = document.createElement('button');
        button.textContent = answer.text;
        button.addEventListener('click', () => handleAnswerClick(answer.isCorrect, button));
        answersContainer.appendChild(button);
    });

    document.getElementById('nextQuestion').style.display = 'none';
}

function handleAnswerClick(isCorrect, button) {
    const answerButtons = document.querySelectorAll('#answers button');
    answerButtons.forEach(btn => btn.disabled = true); // Disable all buttons after selection

    if (isCorrect) {
        score++;
        button.classList.add('correct');
        playSound("correct");
    } else {
        button.classList.add('incorrect');
        document.querySelectorAll('#answers button').forEach(btn => {
            if (btn.textContent === currentQuestions[currentQuestionIndex].answers.find(a => a.isCorrect).text)
                {
                btn.classList.add('correct');
                playSound("wrong");
            }
        });
    }

    document.getElementById('nextQuestion').style.display = 'block'; // Show "Next" button
}


document.getElementById('nextQuestion').addEventListener('click', () => {
    currentQuestionIndex++;
    if (currentQuestionIndex < currentQuestions.length) {
        displayQuestion();
        updateProgress();
    } else {
        finishQuiz();
    }
});


// Function to finish the quiz and show results
function finishQuiz() {
    document.getElementById('quizContainer').style.display = 'none';
    document.getElementById('resultContainer').style.display = 'block';

    document.getElementById('score').textContent = `${score} / ${currentQuestions.length}`;
    document.getElementById('feedback').textContent = getFeedback(score);
}

// Function to generate feedback based on the score
function getFeedback(score) {
    if (score > 8) return "Excellent!";
    if (score > 5) return "Good Job!";
    return "Keep Practicing!";
}

// Utility function to shuffle array (Fisher-Yates shuffle)
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
    return array;
}

// Function to update progress bar
function updateProgress() {
    const progressPercent = ((currentQuestionIndex / currentQuestions.length) * 100) + '%';
    document.getElementById('progress').style.width = progressPercent;
}
function playSound(type) {
    const correctSound = document.getElementById("correctSound");
    const wrongSound = document.getElementById("wrongSound");
    if (type === "correct") {
        correctSound.currentTime = 0; // Reset the sound to the start
        correctSound.play();
    } else if (type === "wrong") {
        wrongSound.currentTime = 0; // Reset the sound to the start
        wrongSound.play();
    }
}