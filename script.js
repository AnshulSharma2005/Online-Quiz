// Handle form submission to fetch questions
document.getElementById('quizForm').addEventListener('submit', function(e) {
    e.preventDefault();

    const classLevel = document.getElementById("class").value;
    const category = document.getElementById("category").value.toLowerCase();
    
    // Get questions based on selected class level and category
    const questions = getQuestionsFromDataset(classLevel, category);

    if (questions.length === 0) {
        alert('No questions available for the selected category. Please try again.');
        return;
    }

    // Set current questions to fetched questions and start the quiz
    currentQuestions = questions.map(q => ({
        text: q.question,
        answers: q.options.map(option => ({
            text: option,
            isCorrect: option === q.answer
        }))
    }));
    
    // Initialize quiz
    currentQuestionIndex = 0;
    score = 0;
    document.getElementById('startContainer').style.display = 'none';
    document.getElementById('quizContainer').style.display = 'block';
    displayQuestion();
});

function formatQuestions(questions) {
    return questions.map(q => ({
        text: q.question,
        answers: shuffleArray([...q.incorrect_answers.map(ans => ({ text: ans, isCorrect: false })), 
                               { text: q.correct_answer, isCorrect: true }])
    }));
}

